#include "../inc/libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	i;
	int	res;

	i = 0;
	sign = 1;
	res = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign *= -1;
		}
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		res = res * 10 + str[i] - 48;
		i++;
	}
	return (sign * res);
}
/* 
white spaces ascii 9 -13 and 32
si sottrae lo 0 cosicche i valori ascii sottratti equivalgano
all'intero desiderato, es : '9' = 57 , '0' = 48. 57 - 48 = 9
*/